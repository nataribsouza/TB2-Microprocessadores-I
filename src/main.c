st_device device_st;

/* Main function */
int main(void) {
  // System config
  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_ADC2_Init();
  MX_I2C2_Init();
  MX_TIM3_Init();

  // Init PWM
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_2);

  // Init LCD display
  init_display();

  // Init device
  init_device(&device_st);

  // Show start message
  display_setCursor(0, 0);
  display_clear();
  display_print(LCD_SCREEN_START);

  // Main infinit loop
  while (1) {
    state_machine(&device_st);
    HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_15);
    HAL_Delay(200);
  }
}

